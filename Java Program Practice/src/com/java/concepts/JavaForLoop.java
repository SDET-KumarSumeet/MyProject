package com.java.concepts;

public class JavaForLoop {

	public static void main(String[] args) {

		// For Loop Example
		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
		// For each Example
		String[] cars = { "Volvo", "BMW", "Ford", "Mazda" };
		for (String i : cars) {
			System.out.println(i);
		}
	}

}
